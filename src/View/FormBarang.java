    private void ShowKategori() {
        con = new Koneksi();
        try {
            st = con.connect().createStatement();
            rs = st.executeQuery("SELECT *FROM tb_kategori");
            while (rs.next()) {
                cbKategori.addItem(rs.getString("nama"));
            }
        } catch (SQLException e) {

        }
    }

