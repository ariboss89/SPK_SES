    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int rowCount = jTable1.getSelectedRow();

        bd.setId(Integer.parseInt(jTable1.getValueAt(rowCount, 0).toString()));
        txtNamaBarang.setText((jTable1.getValueAt(rowCount, 1).toString()));
        cbKategori.setSelectedItem(jTable1.getValueAt(rowCount, 2).toString());

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

